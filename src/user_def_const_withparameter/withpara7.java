package user_def_const_withparameter;

public class withpara7 {

	int x;
	int j;
	String c;
	withpara7(int g,int d,String v)
	{
		x=g;
		j=d;
		c=v;
	}
	public static void main(String[] args) {
		withpara7 go=new withpara7 (67,64,"Good Morning");
		go.my();
		
	}
	
	
	public void my() {
		System.out.println(x);
		System.out.println(j);
		System.out.println(c);

	
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
