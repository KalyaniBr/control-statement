package group1CS;

public class Switch4 {
public static void main(String[] args) {
	switch(5) {
	case 1 :System.out.println("Marathi");
	break;
	case 2 :System.out.println("English");
	break;
	case 3 :System.out.println("Hindi");
	break;
	case 4 :System.out.println("Bengali");
	break;
	case 5 :System.out.println("Tamil");
	break;
	case 6 :System.out.println("Telegu");
	break;
	case 7 :System.out.println("Bihari");
	break;
	default :System.out.println("Invalid input");
	
}
}
}